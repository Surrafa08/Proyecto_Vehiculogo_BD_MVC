    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    String placa = jTextField1.getText();
    String marca = (String) jComboBox2.getSelectedItem();
    String ano = (String) jComboBox1.getSelectedItem();

    String color = "";
    if (jRadioButton1.isSelected()) color = "Rojo";
    else if (jRadioButton2.isSelected()) color = "Azul";
    else if (jRadioButton3.isSelected()) color = "Negro";

    String combustible = "";
    if (jRadioButton4.isSelected()) combustible = "Gasolina";
    else if (jRadioButton5.isSelected()) combustible = "Diesel";
    else if (jRadioButton6.isSelected()) combustible = "Hibrido";

    String caracteristicas = "";
    if (jCheckBox1.isSelected()) caracteristicas += "Aire Acondicionado ";
    if (jCheckBox2.isSelected()) caracteristicas += "Vidrios Electricos ";

    Carro carro = new Carro (placa, marca, ano, color, combustible, caracteristicas);
    controlador.registrarCarro(carro);
    
    }//GEN-LAST:event_jButton1ActionPerformed

