    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
    String nombreUsuario = jTextField1.getText().trim();
    String Contraseña = new String(jTextField2.getPassword());

    if (nombreUsuario.isEmpty() || Contraseña.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar un nombre de usuario", "Campo vacio", javax.swing.JOptionPane.WARNING_MESSAGE);
    return;
    }
    
    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    modelo.addRow(new Object[]{nombreUsuario, Contraseña});
    
    jTextField1.setText("");
    jTextField2.setText("");

    }//GEN-LAST:event_jButton1MouseClicked

